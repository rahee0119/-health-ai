import numpy as np
import pandas as pd
from sklearn.neighbors import KNeighborsClassifier

print("=" * 60)
print("  🤖 공공데이터 기반 생체 수치 예측 및 맞춤형 건강 처방 시스템")
print("=" * 60)

# =========================================================================
# [1단계] 8대 질환 데이터셋 구축 (기립성 저혈압 추가)
# 학습 변수: [공복혈당, 수축기혈압, BMI, 헤모글로빈, 나이]
# 정답(Outcome): 0:정상, 1:당뇨병, 2:역류성식도염, 3:고혈압, 4:비만, 5:심장병, 6:빈혈, 7:뇌졸중, 8:기립성저혈압
# =========================================================================
glucose_list = [135, 95, 140, 110, 185, 105, 90, 150, 100, 95]
bp_list = [145, 115, 125, 135, 155, 120, 110, 140, 130, 90] # 90은 기립성저혈압용 낮은 혈압
bmi_list = [28.5, 22.0, 24.5, 31.2, 21.5, 26.0, 18.0, 29.0, 23.5, 20.1]
hemo_list = [14.5, 15.0, 13.8, 14.2, 15.2, 14.0, 9.5, 13.5, 10.2, 13.0]
age_list = [55, 24, 62, 38, 68, 45, 19, 51, 33, 22]
outcome_list = [1, 0, 3, 2, 7, 4, 6, 5, 0, 8]

medical_data = {
    "Glucose": glucose_list,
    "BloodPressure": bp_list,
    "BMI": bmi_list,
    "Hemoglobin": hemo_list,
    "Age": age_list,
    "Outcome": outcome_list,
}
df = pd.DataFrame(medical_data)

# 머신러닝 변수 분리 및 모델 학습
X = df[["Glucose", "BloodPressure", "BMI", "Hemoglobin", "Age"]]
y = df["Outcome"]

knn_model = KNeighborsClassifier(n_neighbors=3, weights='distance')
knn_model.fit(X, y)

# =========================================================================
# [2단계] 고도화된 생체 수치 추정 설문 알고리즘 (문항 대폭 확장)
# =========================================================================
print("자신의 정확한 검진 수치(혈당, 혈압, BMI, 헤모글로빈)를 알고 계신가요?")
know_data = input("알고 있다면 'Y', 모른다면 'N'을 입력하세요: ").upper()

if know_data == "Y":
    user_glucose = int(input("- 공복 혈당 수치 (mg/dL): "))
    user_bp = int(input("- 수축기 혈압 수치 (mmHg): "))
    user_bmi = float(input("- BMI 체질량지수 (kg/m²): "))
    user_hemo = float(input("- 헤모글로빈 수치 (g/dL): "))
else:
    print("\n[심층 설문조사 모드] 생활 습관과 임상 증상을 종합 분석하여 수치를 정밀 추정합니다.")

    # 1) 혈당(Glucose) 정밀 추정 문항 (식습관 및 피로도)
    print("\n--- [파트 1: 혈당 및 대사 성향 분석] ---")
    q1_1 = int(input("Q1. 탕후루, 탄산음료, 정제 탄수화물(빵, 떡, 면)을 얼마나 자주 드시나요?\n(1: 거의 안 먹음, 2: 주 2~3회, 3: 매일 먹음): "))
    q1_2 = int(input("Q2. 식사 후 참기 힘들 정도로 졸음이 쏟아지거나 쉽게 피로해지시나요?\n(1: 안 고단함, 2: 가끔 졸림, 3: 항상 쏟아짐): "))
    glucose_score = q1_1 + q1_2
    user_glucose = 90 if glucose_score <= 2 else (115 if glucose_score <= 4 else 145)

    # 2) 혈압(BloodPressure) 및 혈관 건강 추정 문항
    print("\n--- [파트 2: 혈압 및 심혈관 부하 분석] ---")
    q2_1 = int(input("Q3. 평소 국물 요리를 끝까지 마시거나 음식을 짜게 드시는 편인가요?\n(1: 싱겁게 먹음, 2: 보통, 3: 매우 짜게 먹음): "))
    q2_2 = int(input("Q4. 부모님이나 형제 중 고혈압, 심장병, 뇌졸중 환자가 있나요?\n(1: 없음, 2: 한 분 계심, 3: 두 분 이상 계심): "))
    q2_3 = int(input("Q5. 갑자기 자리에서 일어날 때 눈앞이 핑 돌며 아찔해지는 어지러움을 느끼시나요?\n(1: 전혀 없음, 2: 가끔 있음, 3: 매우 자주 있음): "))

    bp_score = q2_1 + q2_2
    if q2_3 == 3:  # 기립성 어지러움이 매우 심한 경우 혈압을 낮게 세팅
        user_bp = 90
    else:
        user_bp = 110 if bp_score <= 2 else (128 if bp_score <= 4 else 150)

    # 3) BMI 및 신체 활동 추정 문항
    print("\n--- [파트 3: 신체 계측 및 대사량 분석] ---")
    height = float(input("Q6. 자신의 키를 입력해 주세요 (cm 단위): ")) / 100
    weight = float(input("Q7. 자신의 몸무게를 입력해 주세요 (kg 단위): "))
    user_bmi = round(weight / (height ** 2), 1)

    # 4) 빈혈 및 산소 운반 능력(Hemoglobin) 추정 문항
    print("\n--- [파트 4: 말초 혈액 신호 분석] ---")
    q4_1 = int(input("Q8. 평소 손발이 비정상적으로 차갑거나 안색이 창백하다는 말을 듣나요?\n(1: 아님, 2: 가끔 들음, 3: 자주 들음): "))
    user_hemo = 15.0 if q4_1 == 1 else (13.2 if q4_1 == 2 else 9.6)

user_age = int(input("\n- 마지막으로, 나이 (세)를 입력하세요: "))

# =========================================================================
# [3단계] 머신러닝 분류 알고리즘 연산
# =========================================================================
user_profile = np.array([[user_glucose, user_bp, user_bmi, user_hemo, user_age]])
predicted_class = knn_model.predict(user_profile)[0]
probabilities = knn_model.predict_proba(user_profile)[0]

# =========================================================================
# [4단계] 종합 건강 솔루션 출력 리포트
# =========================================================================
print("\n" + "🩺" * 25)
print("      [ 바이오 인공지능 개인 맞춤형 임상 리포트 ]")
print("🩺" * 25)
print(f"■ 추정 생체 수치: 혈당 {user_glucose}mg/dL | 혈압 {user_bp}mmHg | BMI {user_bmi} | 헤모글로빈 {user_hemo}g/dL")
print("-" * 50)

# 1. 생체 수치 상태 평가 안내 (질병 특정 대신 상태 설명)
print("🔍 [현재 생체 수치 기반 건강 상태 평가]")
if user_glucose >= 126:
    print("- 혈당: 공복 혈당이 높아 세포의 인슐린 저항성이 증가한 상태로 보입니다.")
else:
    print("- 혈당: 항상성이 잘 유지되는 안정적인 혈당 수치입니다.")

if user_bp >= 140:
    print("- 혈압: 혈관 벽이 받는 압력이 높아 심혈관계에 부하가 걸린 상태입니다.")
elif user_bp <= 95:
    print("- 혈압: 수축기 혈압이 낮아 말초 혈액 순환이 저하되거나 기립 시 뇌 혈류량이 순간적으로 감소할 수 있습니다.")
else:
    print("- 혈압: 혈관 탄성도가 양호한 정상 범위의 혈압입니다.")

if user_bmi >= 25:
    print("- 비만도: 체질량지수(BMI)가 높아 대사증후군 위험도가 상승한 상태입니다.")
elif user_bmi < 18.5:
    print("- 비만도: 근육량이나 기초 대사량이 부족할 수 있는 저체중 상태입니다.")
else:
    print("- 비만도: 표준적인 체성분 비율을 유지하고 있습니다.")

if user_hemo < 12.0:
    print("- 혈액 건강: 헤모글로빈 수치가 낮아 체내 산소 공급 효율이 떨어지기 쉬운 상태입니다.")
else:
    print("- 혈액 건강: 적혈구와 산소 결합 상태가 건강합니다.")

print("-" * 50)

# 2. 인공지능 기반 우려 질환 경고
disease_name_map = {
    0: "만성 질환 안전 지대",
    1: "당뇨병",
    2: "역류성 식도염",
    3: "고혈압",
    4: "대사성 비만",
    5: "심장 질환",
    6: "혈액성 빈혈",
    7: "뇌혈관 질환(뇌졸중)",
    8: "기립성 저혈압"
}
print(f"⚠️ [인공지능 경고]: 현재 데이터 패턴 분석 결과, 향후 [ {disease_name_map[predicted_class]} ] 발병 우려가 있으니 각별한 주의가 필요합니다.")
print("-" * 50)

# 3. 행동 처방 가이드 (식습관, 영양제, 운동)
print("📋 [의학 통계 기반 나만의 행동 처방 가이드]")

# 질환군별 맞춤 처방 분기 알고리즘
if predicted_class in [1, 4]: # 당뇨, 비만 우려군
    print("🍏 식습관: 정제 탄수화물과 설탕을 제한하고, 식이섬유가 풍부한 거친 통곡물과 채소 위주 식단을 구성하세요.")
    print("💊 영양제 보충: 혈당 대사와 인슐린 감수성을 고려하여 [바나바잎 추출물], [크롬], [마그네슘] 섭취를 영양 전문가와 상담해보세요.")
    print("🏋️ 운동 습관: 식후 30분 뒤 근육이 포도당을 소모할 수 있도록 30분 이상 빠르게 걷기 및 하체 스쿼트 운동을 하세요.")

elif predicted_class in [3, 5, 7]: # 고혈압, 심장병, 뇌졸중 우려군
    print("🍏 식습관: 체내 나트륨을 배출하기 위해 국물 섭취를 줄이는 저염식을 실천하고, 칼륨이 풍부한 바나나와 아보카도를 드세요.")
    print("💊 영양제 보충: 혈압 조절과 혈관 내피세포 건강에 도움을 주는 [코엔자임Q10], [오메가3], [나토키나제] 섭취를 영양 전문가와 상담해보세요.")
    print("🏋️ 운동 습관: 혈관 탄성도를 높이기 위해 무거운 무게를 드는 것보다 자전거, 수영, 조깅 같은 유산소 운동을 주 4회 이상 하세요.")

elif predicted_class == 8: # 기립성 저혈압 우려군
    print("🍏 식습관: 아침에 일어날 때 수분이 부족하면 혈압이 급격히 떨어지므로, 하루 2L 이상의 물을 충분히 섭취하고 적절한 염분을 유지하세요.")
    print("💊 영양제 보충: 자율신경계 조절과 혈액 순환을 돕는 [비타민 B12], [엽산], [철분] 섭취를 영양 전문가와 상담해보세요.")
    print("🏋️ 운동 습관: 하체에 혈류가 몰리는 것을 막기 위해 평소 종아리와 허벅지 근육을 강화하는 '실내 자전거'와 '발꿈치 들기 운동'을 매일 하세요.")

elif predicted_class == 6: # 빈혈 우려군
    print("🍏 식습관: 철분 흡수율을 높이기 위해 비타민 C가 풍부한 과일과 함께 붉은 살코기, 시금치, 계란 노른자를 자주 섭취하세요.")
    print("💊 영양제 보충: 체내 적혈구 생성을 돕는 [철분제]와 [락토페린] 섭취를 전문가와 상담해보세요.")
    print("🏋️ 운동 습관: 산소 운반 능력이 떨어진 상태이므로 무리한 고강도 운동은 피하고, 가벼운 산책부터 시작해 강도를 늘려가세요.")

else: # 일반 정상군 및 역류성 식도염 등
    print("🍏 식습관: 소화기관에 부담을 주는 야식과 식후 바로 눕는 습관을 버리고, 규칙적인 식사 시간을 유지하세요.")
    print("💊 영양제 보충: 면역력과 대사 흐름을 위해 [종합비타민]과 장 건강을 위한 [유산균]을 권장합니다.")
    print("🏋️ 운동 습관: 하루 7,000보 걷기를 기본 목표로 삼고, 척추 기립근을 펴주는 스트레칭을 생활화하세요.")

